def caesar(message,number):
    text_list = list(message)
    cipher = []
    items = len(text_list)
    for i in range (0,items):
        letter = text_list[i]    
        if direction == "encode": 
            if letter in alphabet:
                    alpha = alphabet.index(letter)
                    alpha = alpha + number
        elif direction == "decode":
            if letter in alphabet:
                    alpha = alphabet.index(letter)
                    alpha = alpha - number
        word = alphabet[alpha]
        cipher.append(word)
    print(" ".join(cipher))
                    

